#include "Items.h"

/**********************************************************************
*
**********************************************************************/
Items::Items() : m_name(""), m_cost(0)
{ }

/**********************************************************************
*
**********************************************************************/
Items::Items(const Items & copy) : m_name(""), m_cost(0)
{
	*this = copy;
}

/**********************************************************************
*
**********************************************************************/
Items::~Items()
{
	m_name = "";
	m_cost = 0;
}

/**********************************************************************
*
**********************************************************************/
Items & Items::operator = (const Items & rhs)
{
	if (this != &rhs)
	{
		m_name = rhs.m_name;
		m_cost = rhs.m_cost;
	}

	return *this;
}

/**********************************************************************
*
**********************************************************************/
String Items::GetName() const
{
	return m_name;
}

/**********************************************************************
*
**********************************************************************/
int Items::GetCost() const
{
	return m_cost;
}

int Items::UseItem()
{
	return 0;
}

